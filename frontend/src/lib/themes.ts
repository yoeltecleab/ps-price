/** Map legacy theme ids stored in localStorage */
export function normalizeThemeId(id: string | null): ThemeId {
  const legacy: Record<string, ThemeId> = {
    cyber: "nebula",
    aurora: "eclipse",
    obsidian: "void",
    plasma: "fusion",
  };
  if (id && themes.some((t) => t.id === id)) return id as ThemeId;
  if (id && legacy[id]) return legacy[id];
  return defaultTheme;
}
